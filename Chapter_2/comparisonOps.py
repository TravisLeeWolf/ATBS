# Comparing my age to 18 using comparison operators
myAge = 26

print('== equal to: ' + str(myAge==18))
print('!= not equal to: ' + str(myAge!=18))
print('< less than: ' + str(myAge<18))
print('> greater than: ' + str(myAge>18))
print('<= less than or equal to: ' + str(myAge<=18))
print('>= greater than or equal to: ' + str(myAge>=18))
